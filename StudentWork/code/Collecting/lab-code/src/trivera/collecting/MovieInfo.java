public class MovieInfo {

	private String name;
	private Double price;

	public MovieInfo(String name, Double price) {
		super();
		this.name = name;
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public Double getPrice() {
		return price;
	}

	public String toString() {
		return String.format("%s, %6.2f", name, price);
	}
}
